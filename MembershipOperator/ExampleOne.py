# These operators test for membership in a sequence
# such as lists, strings or tuples. There are two membership operators that are used in Python. (in, not in).

num = 4
num1 = 8
datalist = [1, 2, 3, 4, 5];

if num in datalist:
    print(("Line 1 - num is available in the given list"))
else:
    print("Line 1 - num is not available in the given list")
if num1 in datalist:
    print(("Line 1 - num1 is available in the given list"))
else:
    print("Line 1 - num1 is not available in the given list")
